text = "7"

print("문자열이 알파벳 또는 숫자로 구성되어 있는지: ", text.isalnum())
print("문자열이 알파벳으로만 구성되어 있는지: ", text.isalpha())
print("문자열이 정수 형태인지: ", text.isdecimal())
print("문자열이 숫자로 인식될 수 있을지: ", text.isdigit())
print("문자열이 공백으로만 구성되어있는지: ", text.isspace())
print("문자열이 대문자로만 구성되어있는지 ", text.isupper())
print("문자열이 소문자로만 구성되어있는지 ", text.islower())

